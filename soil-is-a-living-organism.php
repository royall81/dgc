               <h4>Rhizobacteria</h4>
               <p>
                    <br>Rhizobacteria live close to the plant roots. In addition to making nutrients available to the plant, their presence protects the plant against pathogenic bacteria.
                    Some bacteria create butyric acid, which encourages the plant to develop more roots.
                    The Rhizobacteria do this for a reason; they need the glucose that enters the soil via the plant roots. Rhizobacteria also benefit greatly from the plant growing strong and healthy.<br><br>
               </p>
          </div>
     </div>

     <div class="row pt-3" style="font-size:16px; ">
