          <div class="col-12 col-md-6 px-3 pr-md-0 px-md-5">
               <h4>Mycorrhizal fungi </h4>
               <p>
                    <br>Mycorrhiza is the collective name for beneficial soil fungi. They are found in most natural ecosystems and have a close association with plants.
                    Mycorrhizal fungi need sugars to survive and extract them from the plant's roots. In exchange, they provide water and nutrients from places in the soil that the roots themselves
                    cannot reach.
               </p>
          </div>
     </div>

     <div class="row pt-1" style="font-size:16px; ">
          <div class="col-12 p-2 px-3 pr-md-0 px-md-5">
               <p>
                    <br>Underground, mycorrhizal fungi form a network of microscopically fine fungal filaments. Part of the fungal tissue grows in and around the roots.
                    The other part grows down from the roots deep into the soil. This creates a natural expansion of the root system and increase the absorption capacity through the roots of your plants up to 700%.</p>
               <p>
                    Thanks to the enormous size of the mycorrhiza network, a much larger soil volume can be exploited. Deeper and further water and nutrients are taken up by the
                    fungal threads and transported to the plant.
               </p>
               <p>
                    Plants that grow in soil with a good balance between roots, fungi, and bacteria are better protected against diseases, pests, drought, and stress!<br><br>
               </p>
               <p>
               </p>

               <h4>Potting soil</h4>
               <p>
                    <br>Most potting soil is sterilized by heating. This kills weed seeds and pathogenic bacteria, but also the good fungi and bacteria have disappeared. Important nutrients are often present,
                    but due to a lack of soil life, they cannot be absorbed by the plant.
               </p>
               <p>
                    Administering rhizobacteria and mycorrhiza fungi can therefore directly result in better plant development because more nutrients are available. Plants will be more resilient to
                    infection and the use of fertilizers can be greatly reduced.<br><br>
               </p>


