               <h4><b style="font-weight:400;">Soil is a living organism</b></h4>

               <p>
                    <br>The soil of a healthy forest is a living and self-sufficient ecosystem. All the nutrients that a plant absorbs during its life cycle will sooner or later return to the soil.</p>
               <p>
                    Rhizobacteria and mycorrhiza fungi break down fallen leaves and dead plants into humus. During this mineralization process, nutrients are released and become available again for
