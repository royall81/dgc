               <h1>Mycorrhiza Mix</h1><br>
               <p><b>By adding Mycorrhiza Mix to your soil, your plants grow healthier, which in turn will result in higher yield and better quality, all in a 100% organic way! </b></p>
               <p>What are mycorrhizas? Mycorrhiza fungi are microbial engines that improve plant health and soil quality. They have played a very important role in the nutrient supply of trees and plants for over 400 million years. ­­­ The threads of the mycorrhiza fungi penetrate into the bark cells of the roots and grow into the soil from there. As a result, they form a natural root extension.</p>
               <p>Because the fungal threads are extremely thin, they can reach the smallest bottom pores. There they release minerals bound by acids and enzymes and transport them via the "network" to the roots of the plant. The fungus does this for a very specific reason. In exchange for the nutrients supplied, the mycorrhizal fungi absorb excess glucose from the plant. This creates a perfect form of society called symbiosis.</p>
