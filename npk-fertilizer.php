               <p>
                    After germination, the first set of leaves that are formed are called cotyledons. All the nutrients needed to sustain these first leaves are contained inside the seed. However, as soon as the second set of "real" leaves appears, you should start feeding your seedlings with a diluted fertilizer solution.
