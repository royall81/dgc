                    <h2>What does the NPK mean?</h2><br>
                    <p>The NPK value indicates how much nitrogen, phosphorus, and kalium is in your substrate or fertilizer. All plants need these three essential macronutrients. It is important to know the NPK value of your soil and fertilizer. That way you can give your plants the right amount of nutrients.</p>

                    <p>What do these three macronutrients actually do for your plant?</p>

                    <p><b>(N) Nitrogen</b>. Nitrogen is necessary for the growth of a plant.</p>


               </div>
               <div class="col-12 col-md-6  px-4 px-md-5">


                    <p><b>(P) Phosphorus</b>. Phosphorus is a macronutrient element for any plant. It plays a key role in metabolism and energy transfer. Additional phosphorus is needed in the flowering phase. Phosphorus enhances cell formation in flowers and helps to create roots.</p>

                    <p><b>(K) Kalium</b>. Kalium also known as Potassium is also a macro nutritional element. It is found throughout the plant and is necessary for many of its activities. It is essential for the transport of water and food and it is responsible for the quality and stiffness of the plant. In addition, it manages numerous other processes such as sugar production. Potassium ensures that the plant can produce enough sugars during flowering, which are essential for the development of the flowers.</p>
               </div>
